/* =========================
   SAVE CART
========================= */

function saveCart() {

    localStorage.setItem(

        "4sLankaCart",

        JSON.stringify(cart)

    );


    updateCart();

}


/* =========================
   ADD TO CART
========================= */

function addToCart() {

    const quantityInput =
        document.getElementById(
            "productQuantity"
        );


    const quantity = Math.max(

        1,

        Number(quantityInput.value) || 1

    );


    const cartItemKey =

        selectedProduct.id +
        "-" +
        selectedColour;


    const existingCartItem =
        cart.find(item => {

            return item.key ===
                cartItemKey;

        });


    if (existingCartItem) {

        existingCartItem.quantity +=
            quantity;

    } else {

        cart.push({

            key:
                cartItemKey,

            productId:
                selectedProduct.id,

            colour:
                selectedColour,

            quantity:
                quantity

        });

    }


    saveCart();


    showNotification(
        "Product added to cart"
    );


    closeAllPopups();


    openCart();

}


/* =========================
   UPDATE CART
========================= */

